uint8_t GetFontASCII_8_16(uint8_t code1, uint8_t *pBuff)
{
	uint16_t offset = code1 - 32;
	
	if(offset > ASCII_NUM){
		for(uint16_t i = 0; i < ASCII_EACH_SIZE; i++){
			pBuff[i] = 0;
		}
	}
	else{
		for(uint16_t i = 0; i < ASCII_EACH_SIZE; i++){
			pBuff[i] = ASCII_8_16[offset][i];
		}
	}
	
	return 0;
}

